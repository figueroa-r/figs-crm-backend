package com.rfigueroa.figscrm.dto;

public class CustomerPatchRequest {

    private Integer id;
    private String name;
    private String alias;
    private String companyType;
    private Boolean isActive;
    private Boolean isVerified;
    private String address1;
    private String address2;
    private String city;
    private String state;
    private String zip;

    public CustomerPatchRequest(Integer id, String name, String alias, String companyType, Boolean isActive, Boolean isVerified, String address1, String address2, String city, String state, String zip) {
        this.id = id;
        this.name = name;
        this.alias = alias;
        this.companyType = companyType;
        this.isActive = isActive;
        this.isVerified = isVerified;
        this.address1 = address1;
        this.address2 = address2;
        this.city = city;
        this.state = state;
        this.zip = zip;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getCompanyType() {
        return companyType;
    }

    public void setCompanyType(String companyType) {
        this.companyType = companyType;
    }

    public Boolean getActive() {
        return isActive;
    }

    public void setActive(Boolean active) {
        isActive = active;
    }

    public Boolean getVerified() {
        return isVerified;
    }

    public void setVerified(Boolean verified) {
        isVerified = verified;
    }

    public String getAddress1() {
        return address1;
    }

    public void setAddress1(String address1) {
        this.address1 = address1;
    }

    public String getAddress2() {
        return address2;
    }

    public void setAddress2(String address2) {
        this.address2 = address2;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }
}
